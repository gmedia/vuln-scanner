def _render_pdf_html(job: ScanJobDetailResponse) -> str:
    summary = job.result_summary or {}
    findings_rows = ""
    for f in job.findings:
        sev_class = f"sev-{f.severity}" if f.severity in ("critical", "high", "medium", "low", "info") else ""
        findings_rows += f"""
<tr class="{sev_class}">
  <td><span class="badge badge-{f.severity}">{f.severity.upper()}</span></td>
  <td>{f.category or ''}</td>
  <td>{f.title[:100]}</td>
  <td>{f.cve_id or ''}</td>
  <td>{f.cvss_score or ''}</td>
</tr>"""

    return PDF_TEMPLATE.format(
        target=job.target,
        scan_type=job.scan_type,
        status=job.status,
        duration=f"{(job.completed_at - job.started_at).total_seconds():.0f}s" if job.started_at and job.completed_at else "N/A",
        total_findings=summary.get("total_findings", 0),
        critical=summary.get("critical", 0),
        high=summary.get("high", 0),
        medium=summary.get("medium", 0),
        low=summary.get("low", 0),
        info=summary.get("info", 0),
        exported_at=datetime.now(timezone.utc).strftime("%Y-%m-%d %H:%M:%S UTC"),
        findings_rows=findings_rows,
    )


