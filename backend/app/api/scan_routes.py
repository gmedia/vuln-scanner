@router.post("/scan/mobile", response_model=ScanJobResponse, status_code=202)
async def start_mobile_scan(
    file: UploadFile = File(...),
    platform: str = Form(...),
    db: AsyncSession = Depends(get_db),
):
    if platform not in ("android", "ios"):
        raise HTTPException(status_code=400, detail="platform must be 'android' or 'ios'")
    if not file.filename:
        raise HTTPException(status_code=400, detail="File must have a filename")

    ext = ".apk" if platform == "android" else ".ipa"
    safe_name = os.path.basename(file.filename or f"upload{ext}")
    os.makedirs(UPLOAD_DIR, exist_ok=True)
    file_path = os.path.join(UPLOAD_DIR, f"{os.urandom(8).hex()}_{safe_name}")

    with open(file_path, "wb") as buffer:
        shutil.copyfileobj(file.file, buffer)

    try:
        scan_type = "apk" if platform == "android" else "ipa"
        svc = ScannerService(db)
        job = await svc.start_scan(scan_type=scan_type, target=file.filename, platform=platform, file_path=file_path)
        return job
    except Exception:
        try:
            os.remove(file_path)
        except Exception:
            pass
        raise


